package studentsscore;
import java.util.Scanner;
public class summation10 {

	public static void main(String[] args) {
		int i=0,count=0;
		Scanner sc=new Scanner (System.in);
		System.out.println("enter number:");
		int num=sc.nextInt();
		while(num>=i)
		{
			count++;
			i++;
			
		}
		System.out.println(count);
	}

}
